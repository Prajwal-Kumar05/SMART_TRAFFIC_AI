G = build_graph()
HOSP = HOSPITALS
VEH  = {"Ambulance": {"speed_kmh": 45}, "Blood Van": {"speed_kmh": 50}}

# Snapshot A — used by the routing model when planning the corridor
df_snap_a = df.sort_values("timestamp").groupby("node_id").last().reset_index()

# Snapshot B — independent realisation for post-hoc verification (different seed)
np.random.seed(77)
df_verif = df.copy()
df_verif["congestion"] = (
    df_verif["congestion"] + np.random.normal(0, 0.06, len(df_verif))
).clip(0, 1)
df_snap_b = df_verif.sort_values("timestamp").groupby("node_id").last().reset_index()

total4 = 0; valid4 = 0
yt4, yp4, eta_pred_errs, eta_actual_saves = [], [], [], []

for origin in range(15):
    for h, hi in HOSP.items():
        dest = hi["node"]
        if origin == dest:
            continue
        for vt, vi in VEH.items():
            dist_km, path = dijkstra(G, origin, dest)
            total4 += 1
            if dist_km == float("inf") or len(path) < 2:
                continue
            valid4 += 1
            spd = vi["speed_kmh"]
            eta_naive = (dist_km / spd) * 60

            # Predicted ETA — model planned with Snapshot A
            cumul_a = 0.0
            for i, nid in enumerate(path):
                if i > 0:
                    pnid = path[i-1]
                    cv = df_snap_a[df_snap_a["node_id"] == nid]["congestion"].values
                    cong = float(cv[0]) if len(cv) > 0 else 0.5
                    for a, b, d in EDGES:
                        if (a == pnid and b == nid) or (b == pnid and a == nid):
                            cumul_a += (d / spd) * 60 + cong * 2.5
                            break

            # Actual ETA — verified with independent Snapshot B
            cumul_b = 0.0
            for i, nid in enumerate(path):
                if i > 0:
                    pnid = path[i-1]
                    cv = df_snap_b[df_snap_b["node_id"] == nid]["congestion"].values
                    cong = float(cv[0]) if len(cv) > 0 else 0.5
                    for a, b, d in EDGES:
                        if (a == pnid and b == nid) or (b == pnid and a == nid):
                            cumul_b += (d / spd) * 60 + cong * 2.5
                            break

            # Ground truth: does route actually save time in real conditions?
            yt4.append(1 if cumul_b < eta_naive else 0)
            # Predicted: did the model anticipate time savings?
            yp4.append(1 if cumul_a < eta_naive else 0)

            eta_pred_errs.append(abs(cumul_a - cumul_b))
            eta_actual_saves.append(max(0.0, eta_naive - cumul_b))

vr4 = valid4 / total4
yt4 = np.array(yt4); yp4 = np.array(yp4)
eta_pred_errs    = np.array(eta_pred_errs)
eta_actual_saves = np.array(eta_actual_saves)

a4 = accuracy_score(yt4, yp4)
p4 = precision_score(yt4, yp4, zero_division=1)
r4 = recall_score(yt4, yp4, zero_division=1)
f4 = f1_score(yt4, yp4, zero_division=1)

